package com.its.spiders;

public class CreateListItem {
	public String name;
	public byte[] file;
	public String description;
	
	public CreateListItem() {}
	
	public CreateListItem(String name, String description, byte[] file) {
		this.name = name;
		this.file = file;
		this.name = name;
		this.description = description;
	}
}
